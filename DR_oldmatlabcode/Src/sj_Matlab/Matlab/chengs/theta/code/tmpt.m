% corr: r= 0.536, (0.284, 0.718)   p= 0.00018

z= 0.5*log((1+r)./(1-r));
s= 1./sqrt(n-3);
for i=2:3
    t= (z(i)-z(1))/sqrt(s(1)^2/n(1)+s(i)^2/n(i))
    v= (s(1)^2/n(1)+s(i)^2/n(i))^2/(s(1)^4/(n(1)^2*(n(1)-1))+s(i)^4/(n(i)^2*(n(i)-1)))
    tcdf(t,v)
end

